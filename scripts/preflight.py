#!/usr/bin/env python3
"""preflight.py — one-command wake/pre-push preflight wrapper (stdlib only).

Runs the whole preflight ritual in sequence and reports one PASS/FAIL line per
check, exiting with the WORST (max) exit code so a single red check reds the run:

- `check_sections.py`          — partition: sections ↔ fleet-manifest active lanes.
- `check_ideas.py`             — contents: every idea file parses per the README grammar.
- `check_ideas.py --outbox`    — integrity: outbox proposals ↔ sim-ready ideas.
- `bootstrap.py check --strict --status-only` — control gate: the status heartbeat.
- `preflight.py --gate-wiring` — self-check: the substrate-gate workflow still runs
  this wrapper in its non-control lane (the KIT-OWNED gate file is regenerated by
  every `bootstrap upgrade`, which drops the PR #18 step — happened live in PR #35).
- `preflight.py --open-work`   — ADVISORY ONLY, always exit 0: lists open remote
  branches besides main (`git ls-remote --heads origin`) and uncommitted local state
  (`git status --porcelain`) so a wake/pre-push sees in-flight sibling work BEFORE a
  collision (PRs #38/#39/#40 all leaned on a remembered ad-hoc `ls-remote`). Findings
  are information, never failure — a network blip must not red a PR that changed
  nothing, so this check contributes exit 0 unconditionally and CI stays hermetic.
- `preflight.py --branch-prefix-drift` — ADVISORY ONLY, always exit 0: derives the
  repo's real merged-branch prefixes from local `git log --merges` subjects and warns
  when a RECURRING prefix (>=2 merged branches) matches no
  `substrate.config.json::automerge.branch_patterns` entry — the silent-no-op class
  PR #55 fixed by survey (the kit's default `claude/*` matched zero real branches, so
  the staged auto-merge enabler was disarmed for a whole slice with nothing red
  anywhere). Branch names come from TWO local sources: PR-merge subjects, plus
  `Head-ref: <branch>` provenance lines the auto-merge enabler stamps into squash
  commit bodies (squash subjects carry no branch name — the PR #62 card's 💡; the
  subject leg stays the fallback for pre-provenance history). Local git only
  (hermetic); a shallow clone or provenance-less squash history degrades to an
  informational line, still exit 0.
- `preflight.py --enabler-anchors` — ADVISORY ONLY, always exit 0: asserts BOTH
  host customizations survive in the KIT-OWNED auto-merge enabler (the PR #86
  card-status guard in the arm step's `if`, and the PR #62-lineage `Head-ref:`
  `--body` squash provenance on the arm command). Every adopt/upgrade regenerates
  the enabler and drops both; the kit's carve-out scan is STEP-granular and blind
  to the in-step `--body` edit (proven live on the v1.9.0 upgrade — manual diff
  was the only catch). Report-only on first landing (observe before block — the
  v1.10.0-hop card records the promotion condition).

Convention source: control/README.md § Per-session ritual + README § Landing
conventions — this collects the (previously discipline-only) multi-command ritual
into one command; each check stays independently runnable. Report-only: it never
edits files. Origin: PR #2 session card 💡 (wake-preflight wiring).

Usage:
    python3 scripts/preflight.py

Exit codes: worst child exit — 0 = all green · 1 = violations · 2 = a check could
not run (missing input / unparseable manifest / crashed child, reported as 2).
"""

from __future__ import annotations

import fnmatch
import json
import re
import subprocess
import sys
from pathlib import Path

REPO_ROOT = Path(__file__).resolve().parent.parent
PY = sys.executable or "python3"

# The ritual, in order — ONE edit here per new preflight check (keep in sync with
# control/README.md § Per-session ritual, loudly: a check missing here silently
# falls back to session discipline).
CHECKS: list[tuple[str, list[str]]] = [
    ("check_sections", [PY, "scripts/check_sections.py"]),
    ("check_ideas", [PY, "scripts/check_ideas.py"]),
    ("check_ideas --outbox", [PY, "scripts/check_ideas.py", "--outbox"]),
    ("bootstrap check --strict --status-only",
     [PY, "bootstrap.py", "check", "--strict", "--status-only"]),
    ("gate-wiring self-check",
     [PY, "scripts/preflight.py", "--gate-wiring"]),
    ("open-work advisory (report-only, never gates)",
     [PY, "scripts/preflight.py", "--open-work"]),
    ("branch-prefix-drift advisory (report-only, never gates)",
     [PY, "scripts/preflight.py", "--branch-prefix-drift"]),
    ("enabler-anchors advisory (report-only, never gates)",
     [PY, "scripts/preflight.py", "--enabler-anchors"]),
]

GATE_FILE = ".github/workflows/substrate-gate.yml"
PREFLIGHT_RUN = "run: python3 scripts/preflight.py"
NON_CONTROL_GUARD = "steps.lane.outputs.control_only != 'true'"


def check_gate_wiring() -> int:
    """Assert the gate↔ritual convergence step (PR #18) is still wired.

    The gate file is KIT-OWNED — every `bootstrap upgrade` regenerates it in place
    and drops the wake-preflight step (happened live in PR #35; v1.7.1 banks a
    backup under `.substrate/backup/` but the re-apply is manual). This check makes
    the drop loud: red unless the NON-CONTROL lane still runs this wrapper.
    """
    gate = REPO_ROOT / GATE_FILE
    try:
        text = gate.read_text(encoding="utf-8")
    except OSError as exc:
        print(f"gate-wiring: could not read {GATE_FILE}: {exc}", file=sys.stderr)
        return 2
    # Step granularity of the kit-planted gate: each step starts `\n      - `.
    # The wake-preflight step = the wrapper invocation guarded by the
    # non-control-lane condition, in one step block (anchors per PR #18).
    if any(PREFLIGHT_RUN in step and NON_CONTROL_GUARD in step
           for step in text.split("\n      - ")):
        print(f"gate-wiring: OK — {GATE_FILE} non-control lane runs scripts/preflight.py")
        return 0
    print(
        f"gate-wiring: RED — {GATE_FILE} lost the wake-preflight step "
        f"(`{PREFLIGHT_RUN}` guarded by `if: {NON_CONTROL_GUARD}`). The gate file "
        "is KIT-OWNED: a `bootstrap upgrade` regenerated it and dropped the PR #18 "
        "step (as happened live in PR #35). RE-APPLY per the PR #18 recipe — insert "
        "the step between `setup-python` and the session-card gate step (recipe: "
        ".sessions/2026-07-10-gate-ritual-convergence.md § 💡; banked pre-regen "
        "copies live under .substrate/backup/).",
        file=sys.stderr,
    )
    return 1


def _first_line(text: str | None) -> str:
    """First non-empty line of a (possibly multi-line) error, for one-line advisories."""
    for line in (text or "").splitlines():
        if line.strip():
            return line.strip()
    return ""


def check_open_work() -> int:
    """Open-work sweep — ADVISORY ONLY, unconditionally exit 0 (never gates).

    Lists in-flight sibling work at wake/pre-push time: open remote branches besides
    main, plus uncommitted local state. Report-only by construction (the
    harvest-freshness probe's hermeticity rule, ideas/fleet/
    open-work-preflight-sweep-2026-07-10.md Q4): findings, empty findings, and even a
    dead network all PASS — the value is the listing, the exit code carries nothing.
    Origin: PR #40 card 💡 (each repo builds its own open-work ritual step).
    """
    # Leg 1: open remote branches besides main (git transport only — universal).
    try:
        proc = subprocess.run(
            ["git", "ls-remote", "--heads", "origin"],
            cwd=REPO_ROOT, capture_output=True, text=True, timeout=30,
        )
        if proc.returncode != 0:
            raise RuntimeError(_first_line(proc.stderr) or f"exit {proc.returncode}")
        heads = [
            (sha[:9], ref[len("refs/heads/"):])
            for line in proc.stdout.splitlines()
            if line.strip()
            for sha, _, ref in [line.partition("\t")]
            if ref.startswith("refs/heads/") and ref != "refs/heads/main"
        ]
        if heads:
            print(f"open-work: {len(heads)} open remote branch(es) besides main "
                  "(advisory — a sibling may be in flight; merge forward-only, "
                  "never rebase):")
            for sha, name in heads:
                print(f"open-work:   {sha}  {name}")
        else:
            print("open-work: no open remote branches besides main")
    except Exception as exc:  # advisory only — a failed sweep is never a failed gate
        print(f"open-work: could not list remote branches ({_first_line(str(exc))}) "
              "— advisory only, still PASS")

    # Leg 2: uncommitted local state (never relevant in CI's clean checkout; at a
    # wake it flags a sibling's — or a crashed session's — half-done tree).
    try:
        proc = subprocess.run(
            ["git", "status", "--porcelain"],
            cwd=REPO_ROOT, capture_output=True, text=True, timeout=30,
        )
        if proc.returncode != 0:
            raise RuntimeError(_first_line(proc.stderr) or f"exit {proc.returncode}")
        dirt = [line for line in proc.stdout.splitlines() if line.strip()]
        if dirt:
            print(f"open-work: {len(dirt)} uncommitted local path(s) (advisory):")
            for line in dirt:
                print(f"open-work:   {line}")
        else:
            print("open-work: working tree clean")
    except Exception as exc:
        print(f"open-work: could not read local git status ({_first_line(str(exc))}) "
              "— advisory only, still PASS")

    print("open-work: OK — advisory sweep complete (report-only, never affects the exit)")
    return 0


# --branch-prefix-drift anchors: the PR-merge subject GitHub writes on merge-commit
# landings ("Merge pull request #N from <owner-or-fork>/<branch>"), the `Head-ref:`
# provenance line the auto-merge enabler stamps into squash commit bodies (squash
# subjects carry no branch name — `gh pr merge --auto --squash --body`, see
# .github/workflows/auto-merge-enabler.yml), the config key the auto-merge enabler
# is generated from, and the recurrence bar that separates a convention (>=2 merged
# branches) from a one-off branch name.
MERGE_SUBJECT_RE = re.compile(r"^Merge pull request #\d+ from [^/\s]+/(\S+)$")
HEAD_REF_BODY_RE = re.compile(r"^Head-ref: (\S+)$", re.MULTILINE)
CONFIG_FILE = "substrate.config.json"
DRIFT_RECURRENCE = 2
MERGE_LOG_LIMIT = 200


def _prefix_pattern(branch: str) -> str:
    """Group key for a merged-branch name, in branch_patterns' own vocabulary.

    First path segment + "/*" (`probe/x` → `probe/*`); a slashless name falls back
    to its first hyphen token + "-*" (`seed-games-…` → `seed-*` — the one real
    slashless convention in this repo's history, which a naive `<seg>/*` key would
    have hidden).
    """
    if "/" in branch:
        return branch.split("/", 1)[0] + "/*"
    return branch.split("-", 1)[0] + "-*"


def check_branch_prefix_drift(config_path: Path | None = None) -> int:
    """Branch-prefix drift tripwire — ADVISORY ONLY, unconditionally exit 0.

    Compares the branch prefixes this repo ACTUALLY merges (parsed from local
    git history — hermetic, no network) against
    `substrate.config.json::automerge.branch_patterns`, and reports any RECURRING
    prefix (>= DRIFT_RECURRENCE merged branches) that matches no configured pattern.
    That mismatch is the exact silent-disarm class PR #55 fixed by hand survey: the
    enabler never arms on an unmatched prefix, nothing goes red, and auto-merge
    quietly stops being structural. Report-only by construction (the open-work
    advisory's hermeticity rule): findings, empty findings, a shallow clone, a
    provenance-less squash window, and an unreadable config all PASS — the value is
    the listing, the exit code carries nothing. TWO branch-name sources: (1)
    `git log --merges` PR-merge subjects ("Merge pull request #N from owner/branch"
    — merge-commit landings only), and (2) `Head-ref: <branch>` provenance lines in
    squash commit bodies, stamped at arm time by the auto-merge enabler (squash
    subjects are `<title> (#N)` with no branch name — the idea file's Q4 blind
    spot, thinned by the provenance line from the PR that shipped it onward; the
    subject leg stays the fallback for the pre-provenance history).

    `config_path` overrides the config location — the planted-violation smoke-test
    seam (point it at a doctored copy; production callers pass nothing).
    """
    cfg = config_path if config_path is not None else REPO_ROOT / CONFIG_FILE
    try:
        patterns = json.loads(cfg.read_text(encoding="utf-8")).get(
            "automerge", {}).get("branch_patterns")
    except Exception as exc:
        print(f"branch-prefix-drift: could not read {cfg.name} "
              f"({_first_line(str(exc))}) — advisory only, still PASS")
        return 0
    if not isinstance(patterns, list) or not all(isinstance(p, str) for p in patterns):
        print("branch-prefix-drift: no automerge.branch_patterns list configured — "
              "nothing to compare (enabler not adopted?); advisory only, still PASS")
        return 0

    try:
        proc = subprocess.run(
            ["git", "log", "--merges", "-n", str(MERGE_LOG_LIMIT), "--pretty=%s"],
            cwd=REPO_ROOT, capture_output=True, text=True, timeout=30,
        )
        if proc.returncode != 0:
            raise RuntimeError(_first_line(proc.stderr) or f"exit {proc.returncode}")
        subjects = proc.stdout.splitlines()
    except Exception as exc:  # advisory only — a failed survey is never a failed gate
        print(f"branch-prefix-drift: could not read git merge history "
              f"({_first_line(str(exc))}) — advisory only, still PASS")
        return 0

    branches = [m.group(1) for s in subjects if (m := MERGE_SUBJECT_RE.match(s))]

    # Provenance leg: squash landings are NOT merge commits and their default
    # subject carries no branch name, so read the `Head-ref: <branch>` line the
    # auto-merge enabler stamps into squash commit bodies at arm time. The
    # subject leg above stays the fallback for pre-provenance history (and any
    # future merge-commit landings).
    provenance: list[str] = []
    try:
        proc = subprocess.run(
            ["git", "log", "--no-merges", "-n", str(MERGE_LOG_LIMIT), "--pretty=%b"],
            cwd=REPO_ROOT, capture_output=True, text=True, timeout=30,
        )
        if proc.returncode != 0:
            raise RuntimeError(_first_line(proc.stderr) or f"exit {proc.returncode}")
        provenance = HEAD_REF_BODY_RE.findall(proc.stdout)
    except Exception as exc:  # advisory only — the subject leg already ran
        print(f"branch-prefix-drift: could not read squash-provenance bodies "
              f"({_first_line(str(exc))}) — continuing on merge subjects alone")
    branches += provenance

    if not branches:
        print("branch-prefix-drift: no PR merge subjects or Head-ref provenance "
              "lines in local history (shallow clone or pre-provenance squash "
              "window) — nothing to compare, still PASS")
        return 0

    uncovered: dict[str, list[str]] = {}
    for branch in branches:
        if any(fnmatch.fnmatchcase(branch, pat) for pat in patterns):
            continue
        uncovered.setdefault(_prefix_pattern(branch), []).append(branch)
    drifted = {k: v for k, v in uncovered.items() if len(v) >= DRIFT_RECURRENCE}

    if drifted:
        print(f"branch-prefix-drift: DRIFT (advisory) — {len(drifted)} recurring "
              "merged-branch prefix(es) match NO automerge.branch_patterns entry "
              "(the enabler never arms on these — the PR #55 silent-no-op class; "
              "add the pattern or retire the convention):")
        for prefix, names in sorted(drifted.items(), key=lambda kv: -len(kv[1])):
            sample = ", ".join(names[:3]) + (" …" if len(names) > 3 else "")
            print(f"branch-prefix-drift:   {prefix}  ×{len(names)}  (e.g. {sample})")
    else:
        singles = sum(len(v) for v in uncovered.values())
        print(f"branch-prefix-drift: OK — every recurring merged-branch prefix "
              f"matches a configured pattern ({len(branches)} merged branches "
              f"surveyed ({len(provenance)} via Head-ref provenance), "
              f"{len(patterns)} patterns, {singles} one-off unmatched "
              "branch(es) below the recurrence bar)")
    print("branch-prefix-drift: OK — advisory sweep complete "
          "(report-only, never affects the exit)")
    return 0


# --enabler-anchors anchors: the two HOST customizations living in the KIT-OWNED
# auto-merge enabler (regenerated in place by every adopt/upgrade). The kit's
# carve-out scan is STEP-granular: it names the whole added card-guard step but is
# blind to the `--body` provenance edit INSIDE the kit's own arm step (proven live
# on the v1.9.0 upgrade — only the #86-prescribed manual diff caught it). These are
# the exact byte-needles the re-apply duty must preserve.
ENABLER_FILE = ".github/workflows/auto-merge-enabler.yml"
ENABLER_ANCHORS: list[tuple[str, str]] = [
    ("card-status guard (PR #86 — arm-step `if` gates on the card step's output)",
     "steps.card.outputs.skip == '0'"),
    ("Head-ref squash provenance (PR #62/#64 — `--body` stamps the source branch)",
     '--body "Head-ref: $HEAD_REF"'),
]


def check_enabler_anchors() -> int:
    """Enabler-anchor tripwire — ADVISORY ONLY, unconditionally exit 0 (v1.10.0 hop).

    Asserts BOTH host customizations still live in the kit-owned auto-merge enabler:
    the PR #86 card-status guard (the arm step's `if` consults
    `steps.card.outputs.skip`) and the PR #62-lineage `Head-ref:` squash provenance
    (`--body` on the arm command). Every adopt/upgrade regenerates the enabler and
    drops both (v1.9.0 upgrade: the carve-out scan named the guard STEP but was
    step-blind to the in-step `--body` edit — manual diff was the only catch, and
    manual-diff duties are exactly what tripwires exist to replace, per the
    gate-wiring precedent that has caught its class four upgrades running).
    REPORT-ONLY on first landing (deliberate, this hop's card § advisory-mode
    decision): a brand-new check observes before it blocks — if the needle bytes or
    the scan itself are miscalibrated, an advisory misreport costs one log line, a
    red would jam every PR; promote to gating (return 1) after it survives a real
    upgrade cycle. Findings, empty findings, and an unreadable file all PASS — the
    value is the listing, the exit code carries nothing (the open-work advisory's
    hermeticity rule).
    """
    enabler = REPO_ROOT / ENABLER_FILE
    try:
        text = enabler.read_text(encoding="utf-8")
    except OSError as exc:  # advisory only — a missing enabler is adopt's business
        print(f"enabler-anchors: could not read {ENABLER_FILE} "
              f"({_first_line(str(exc))}) — advisory only, still PASS")
        return 0
    missing = [(label, needle) for label, needle in ENABLER_ANCHORS
               if needle not in text]
    if missing:
        print(f"enabler-anchors: DRIFT (advisory) — {len(missing)} of "
              f"{len(ENABLER_ANCHORS)} host customization anchor(s) MISSING from "
              f"{ENABLER_FILE} (the file is KIT-OWNED: an adopt/upgrade regenerated "
              "it and dropped the host edits — the v1.9.0-upgrade class; RE-APPLY "
              "per the re-apply duty, banked pre-regen copies live under "
              ".substrate/backup/):")
        for label, needle in missing:
            print(f"enabler-anchors:   MISSING {label} — needle `{needle}`")
    else:
        print(f"enabler-anchors: OK — both host customization anchors present in "
              f"{ENABLER_FILE} (card-status guard + Head-ref --body provenance)")
    print("enabler-anchors: OK — advisory sweep complete "
          "(report-only, never affects the exit)")
    return 0


def main() -> int:
    worst = 0
    for name, cmd in CHECKS:
        try:
            code = subprocess.run(cmd, cwd=REPO_ROOT).returncode
        except OSError as exc:  # fail loud: a check that cannot run is not a pass
            print(f"preflight: could not run {name}: {exc}", file=sys.stderr)
            code = 2
        verdict = "PASS" if code == 0 else "FAIL"
        print(f"preflight: {verdict} — {name} (exit {code})")
        worst = max(worst, code)
    if worst == 0:
        print(f"preflight: OK — all {len(CHECKS)} checks green")
    else:
        print(f"preflight: FAIL — worst exit {worst}")
    return worst


if __name__ == "__main__":
    if "--gate-wiring" in sys.argv[1:]:
        sys.exit(check_gate_wiring())
    if "--open-work" in sys.argv[1:]:
        sys.exit(check_open_work())
    if "--enabler-anchors" in sys.argv[1:]:
        sys.exit(check_enabler_anchors())
    if "--branch-prefix-drift" in sys.argv[1:]:
        args = sys.argv[1:]
        cfg = None
        if "--config" in args and args.index("--config") + 1 < len(args):
            cfg = Path(args[args.index("--config") + 1])
        sys.exit(check_branch_prefix_drift(cfg))
    sys.exit(main())
